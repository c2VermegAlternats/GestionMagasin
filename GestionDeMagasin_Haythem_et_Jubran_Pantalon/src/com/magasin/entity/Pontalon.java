package com.magasin.entity;

public class Pontalon {

	private String tissue;
	private String fit;
	
	
	public Pontalon() {
		super();
	}


	public Pontalon(String tissue, String fit) {
		super();
		this.tissue = tissue;
		this.fit = fit;
	}


	public String getTissue() {
		return tissue;
	}


	public void setTissue(String tissue) {
		this.tissue = tissue;
	}


	public String getFit() {
		return fit;
	}


	public void setFit(String fit) {
		this.fit = fit;
	}
	
	
	
	
	
}
